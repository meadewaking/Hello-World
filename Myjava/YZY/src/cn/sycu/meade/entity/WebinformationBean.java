package cn.sycu.meade.entity;

public class WebinformationBean {
	private int webInformationId = 0;
	private String webInformationName = "";
	private String content = "";
	private String dercription = "";

	public int getWebInformationId() {
		return webInformationId;
	}

	public void setWebInformationId(int webInformationId) {
		this.webInformationId = webInformationId;
	}

	public String getWebInformationName() {
		return webInformationName;
	}

	public void setWebInformationName(String webInformationName) {
		this.webInformationName = webInformationName;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getDercription() {
		return dercription;
	}

	public void setDercription(String dercription) {
		this.dercription = dercription;
	}

}
